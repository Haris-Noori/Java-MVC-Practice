package pkg;

public class StudentModel {

	
	private String rollNo;
	private String name;
	
	public void setRollNo(String RollNo) //set rollno
	{
		rollNo = RollNo;
	}
	
	public void setName(String Name) // set name
	{
		name = Name;
	}
	
	public String getRollNo()  // get rollno
	{
		return rollNo;
	}
	
	public String getName()   // get name
	{
		return name;
	}
	
	
	
}
